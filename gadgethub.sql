-- Create Database
CREATE DATABASE IF NOT EXISTS GADGETHUB;
USE GADGETHUB;

-- ================= USERS =================
CREATE TABLE USERS (
    USEREMAIL VARCHAR(60) PRIMARY KEY,
    USERNAME VARCHAR(30),
    MOBILE VARCHAR(15),
    ADDRESS VARCHAR(250),
    PINCODE INT,
    PASSWORD VARCHAR(20)
);

-- ================= PRODUCTS =================
CREATE TABLE PRODUCTS (
    PID VARCHAR(45) PRIMARY KEY,
    PNAME VARCHAR(100),
    PTYPE VARCHAR(20),
    PINFO VARCHAR(350),
    PPRICE DECIMAL(12,2),
    PQUANTITY DECIMAL(18,6),
    IMAGE LONGBLOB,
    AVAILABLE CHAR(1)
);

-- ================= ORDERS =================
CREATE TABLE ORDERS (
    ORDERID VARCHAR(45),
    PRODID VARCHAR(45),
    QUANTITY DECIMAL(18,6),
    AMOUNT DECIMAL(10,2),
    SHIPPED TINYINT,
    PRIMARY KEY (ORDERID, PRODID),
    FOREIGN KEY (PRODID) REFERENCES PRODUCTS(PID)
);

-- ================= TRANSACTIONS =================
CREATE TABLE TRANSACTIONS (
    TRANSID VARCHAR(45) PRIMARY KEY,
    USEREMAIL VARCHAR(60),
    TRANSTIME DATETIME,
    AMOUNT DECIMAL(10,2),
    FOREIGN KEY (USEREMAIL) REFERENCES USERS(USEREMAIL)
);

-- ================= USERCART =================
CREATE TABLE USERCART (
    USEREMAIL VARCHAR(60),
    PRODID VARCHAR(45),
    QUANTITY DECIMAL(18,6),
    FOREIGN KEY (USEREMAIL) REFERENCES USERS(USEREMAIL),
    FOREIGN KEY (PRODID) REFERENCES PRODUCTS(PID)
);

-- ================= USERDEMAND =================
CREATE TABLE USERDEMAND (
    USEREMAIL VARCHAR(60),
    PRODID VARCHAR(45),
    QUANTITY DECIMAL(18,6),
    PRIMARY KEY (USEREMAIL, PRODID),
    FOREIGN KEY (USEREMAIL) REFERENCES USERS(USEREMAIL),
    FOREIGN KEY (PRODID) REFERENCES PRODUCTS(PID)
);

